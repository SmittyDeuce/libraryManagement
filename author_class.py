class Author():
    def __init__(self, name, age, origin_place, zodiac):
        # Constructor method to initialize an instance of the Author class
        self.name = name  # Assign the name parameter to the name attribute
        self.age = age    # Assign the age parameter to the age attribute
        self.origin_place = origin_place  # Assign the origin_place parameter to the origin_place attribute
        self.zodiac = zodiac  # Assign the zodiac parameter to the zodiac attribute